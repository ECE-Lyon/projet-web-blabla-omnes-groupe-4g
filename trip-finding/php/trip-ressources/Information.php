          <ul>
            <li>
            Photo du conducteur : Vous pouvez voir une photo du conducteur.
            </li>
            <li>
            Trajet : Les détails du trajet proposé.
            </li>
            <li>
            Prix : Le coût du trajet.
            </li>
          </ul>
