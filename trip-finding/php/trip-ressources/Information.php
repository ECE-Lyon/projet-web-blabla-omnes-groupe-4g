        <p>Sur BlaBlaCar, vous pouvez rechercher des trajets en remplissant un formulaire de recherche. Ce formulaire comprend les informations suivantes :<br>
          <ul>
            <li>
            Départ : Le point de départ de votre voyage.
            </li>
            <li>
            Destination : Votre destination souhaitée.
            </li>
            <li>
            Date : La date à laquelle vous souhaitez voyager.
            </li>
            <li>
            Nombre de passagers : Le nombre de passagers qui voyageront avec vous.
            </li>
          </ul>
