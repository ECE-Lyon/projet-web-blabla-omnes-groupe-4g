          <ul>
            <li>
            Modèle de voiture : Le type de véhicule utilisé pour le trajet.
            </li>
            <li>
            Préférences du conducteur : Les préférences ou conditions spéciales du conducteur (comme les préférences musicales, les discussions pendant le trajet, etc.).
            </li>
          </ul>
          Pour confirmer un trajet, vous devez vous connecter à votre compte BlaBlaCar. Si vous n'avez pas encore de compte, vous devrez en créer un.<br>
          Après avoir choisi et payé le trajet, le conducteur est automatiquement informé qu'il a un nouveau passager. Le conducteur peut avoir la possibilité d'accepter ou de refuser le passager, selon les paramètres du trajet.<br>
          Une fois que le conducteur a accepté le trajet (automatiquement ou manuellement), les coordonnées téléphoniques sont échangées.<br>
          Une fois le trajet effectué, le conducteur reçoit le paiement sur son compte BlaBlaCar.
